import java.util.*;
public class basic{
    public static int functionOne(){
        System.out.println("This is the first function.");
        return 0;
    }
    public static void main(String args[]){
        functionOne();

    }
}


// function parameters and arguments

// the values passed when the function defined are called parameters, and the values passed 
// when the function is called are called arguments.


// memory taken by function
// the memory taken by function is called stack memory, and the memory taken by main function is called heap memory.
// the stack memory is used to store the function calls and local variables, and the heap memory is used to store the objects.

// function takes memory in the form of call stack
// // the call stack is a data structure that stores the function calls and local variables.

